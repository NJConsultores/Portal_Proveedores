package com.infra.factura.dto.html;

import java.util.ArrayList;
import java.util.List;

public class FeRespuestaReenvioFacturaHtml {
	private Boolean status;
	private List<String> procesadas;
	private List<String> errores;
	private List<String> desconocidas;
	
	
	public FeRespuestaReenvioFacturaHtml() {
		procesadas = new ArrayList<String>();
		errores = new ArrayList<String>();
		desconocidas = new ArrayList<String>();
	}
	
	public Boolean getStatus() {
		return status;
	}
	
	public void setStatus(Boolean status) {
		this.status = status;
	}
	
	public List<String> getProcesadas() {
		return procesadas;
	}
	
	public void setProcesadas(List<String> procesadas) {
		this.procesadas = procesadas;
	}
	
	public List<String> getErrores() {
		return errores;
	}
	
	public void setErrores(List<String> errores) {
		this.errores = errores;
	}

	public List<String> getDesconocidas() {
		return desconocidas;
	}

	public void setDesconocidas(List<String> desconocidas) {
		this.desconocidas = desconocidas;
	}
	
	

}
